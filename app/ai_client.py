import os
from openai import OpenAI

client = None


def init_openai():
    """–ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç OpenAI –∫–ª–∏–µ–Ω—Ç."""
    global client
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        raise ValueError("OPENAI_API_KEY –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è")
    client = OpenAI(api_key=api_key)


def summarize_email(text: str) -> str:
    """
    –°–æ–∑–¥–∞—ë—Ç –∫—Ä–∞—Ç–∫–æ–µ —Ä–µ–∑—é–º–µ –ø–∏—Å—å–º–∞ (1-3 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è).
    
    Args:
        text: –¢–µ–∫—Å—Ç –ø–∏—Å—å–º–∞
        
    Returns:
        –ö—Ä–∞—Ç–∫–æ–µ —Ä–µ–∑—é–º–µ –ø–∏—Å—å–º–∞
    """
    if not client:
        init_openai()
    
    try:
        response = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {
                    "role": "system",
                    "content": "–°–¥–µ–ª–∞–π –∫—Ä–∞—Ç–∫–æ–µ —Ä–µ–∑—é–º–µ –ø–∏—Å—å–º–∞ –Ω–∞ 1-3 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è. –°–æ—Ö—Ä–∞–Ω–∏ –≤–∞–∂–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é: –æ—Ç –∫–æ–≥–æ, –æ —á—ë–º, —á—Ç–æ —Ç—Ä–µ–±—É–µ—Ç—Å—è."
                },
                {
                    "role": "user",
                    "content": text
                }
            ],
            temperature=0.3,
            max_tokens=200
        )
        
        return response.choices[0].message.content.strip()
    except Exception as e:
        return f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ä–µ–∑—é–º–µ: {str(e)}"


